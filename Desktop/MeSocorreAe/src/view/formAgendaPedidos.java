    public void atualizaTabela(){
        listaPeds = new ArrayList<>();
        listaPeds = conexaoController.getListaServicos(
                conexaoController.getUsuarioLogado().getCpf(),
                jCBPendentes.isSelected(),
                jCBAceitos.isSelected(),
                jCBRecusados.isSelected(),
                jCBConcluidos.isSelected());
        pedidosModel = new AgendaPedidosTableModel(listaPeds);
        jTTabPedidos.setModel(pedidosModel);
    }

