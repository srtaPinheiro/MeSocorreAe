    private void jBDetalharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDetalharActionPerformed
        if (jTTabPedidos.getSelectedRow() > -1) {
            int codPed = listaPeds.get(jTTabPedidos.getSelectedRow()).getCod();
            formDetalheServico fds = new formDetalheServico(codPed);
            fds.setModal(true);
            fds.setVisible(true);
        }
    }//GEN-LAST:event_jBDetalharActionPerformed

